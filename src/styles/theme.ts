export const getTheme =
  <T1 extends keyof Theme, T2 extends keyof Theme[T1]>(
    val1: T1,
    val2: T2,
  ): any =>
  ({ theme }: { theme: Theme }): Theme[T1][T2] =>
    theme[val1][val2]

